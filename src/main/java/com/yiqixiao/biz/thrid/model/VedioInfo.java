public class VedioInfo {

	private String vid;// 视频ID
	private String tag;// 视频标签
	private String title;// 视频标题
	private String desc;// 视频描述
	private String img;// 视频截图
	private String mimg;// 视频截图(中)
	private String bimg;// 视频截图(大)
	private String chk_yn;// y-通过审核，n-未通过审核
	private String totaltime;// int64 视频总时长
	private String swf;// 视频播放地址
	private String code;// 错误码,cod=0为成功
	/**
	 * @return the vid
	 */
	public String getVid() {
		return vid;
	}
	/**
	 * @param vid the vid to set
	 */
	public void setVid(String vid) {
		this.vid = vid;
	}
	/**
	 * @return the tag
	 */
	public String getTag() {
		return tag;
	}
	/**
	 * @param tag the tag to set
	 */
	public void setTag(String tag) {
		this.tag = tag;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the desc
	 */
	public String getDesc() {
		return desc;
	}
	/**
	 * @param desc the desc to set
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}
	/**
	 * @return the img
	 */
	public String getImg() {
		return img;
	}
	/**
	 * @param img the img to set
	 */
	public void setImg(String img) {
		this.img = img;
	}
	/**
	 * @return the mimg
	 */
	public String getMimg() {
		return mimg;
	}
	/**
	 * @param mimg the mimg to set
	 */
	public void setMimg(String mimg) {
		this.mimg = mimg;
	}
	/**
	 * @return the bimg
	 */
	public String getBimg() {
		return bimg;
	}
	/**
	 * @param bimg the bimg to set
	 */
	public void setBimg(String bimg) {
		this.bimg = bimg;
	}
	/**
	 * @return the chk_yn
	 */
	public String getChk_yn() {
		return chk_yn;
	}
	/**
	 * @param chk_yn the chk_yn to set
	 */
	public void setChk_yn(String chk_yn) {
		this.chk_yn = chk_yn;
	}
	/**
	 * @return the totaltime
	 */
	public String getTotaltime() {
		return totaltime;
	}
	/**
	 * @param totaltime the totaltime to set
	 */
	public void setTotaltime(String totaltime) {
		this.totaltime = totaltime;
	}
	/**
	 * @return the swf
	 */
	public String getSwf() {
		return swf;
	}
	/**
	 * @param swf the swf to set
	 */
	public void setSwf(String swf) {
		this.swf = swf;
	}
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	
	
	
	

}
